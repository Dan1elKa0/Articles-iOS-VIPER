import UIKit
import UIColor_Hex_Swift


extension UIColor
{
    class func backgroundWhite() -> UIColor
    {
        return UIColor(rgba: "#dadada")
    }


    class func black_87() -> UIColor
    {
        return UIColor(rgba: "#000000de")
    }


    class func black_54() -> UIColor
    {
        return UIColor(rgba: "#0000008a")
    }
}
